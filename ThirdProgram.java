// Public is the access specific, and the class is the block of code with the name of ThridProgram
public class ThirdProgram {
	
	public static void main(String [] args) {
		
		// Printing  with several instructions
		System.out.println();
		System.out.print("This is from memory. ");
		System.out.print("Made it through the first week of class,");
		System.out.println(" and I'm ready for the weekend.");
		System.out.println("Jim says, \"I agree\".");
	
		// Printing the same thing with only one instruction
		System.out.print("This is from memory. " +
						"Made it through the first week of class," +
						" and I'm ready for the weekend." +
						"\nJim says, \"I agree\".");
		System.out.println();
	
		// Fun with escape sequences
		System.out.println("\n\n\tDave thinks this f\bunny. \\-\\");
		
		// This enters down two lines, tabs it over, deletes the f and prints unny, and then prints \-\
		
	/* Escape Sequences
	\" prints a double quotation without counting the quotation as part of the method 
	\' prints a single quotation
	\n prints an enter (a new line for the text)
	\t  prints a tab
	\b prints a backspace
	\\ prints a backslash */
	
	
	}
	

}